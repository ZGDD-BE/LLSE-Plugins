/**
 * 加载器版本对象
 */ 
class ObjectVersion {
  
/**
 * 主版本号（如 1.0.2 里的 1）
 * @type Integer
 */ 
 static major;
/**
 * 次版本号（如 1.0.2 里的 0）
 * @type Integer
 */ 
 static minor;
/**
 * 修订版本号（如 1.0.2 里的 2）
 * @type Integer
 */ 
 static revision;
/**
 * 当前版本是否为测试版
 * @type Boolean
 */ 
 static isBeta;
  
  
  



}
