/**
 * 获取一个设备信息对象
 */ 
class Device {
  
/**
 * 玩家设备的IP地址
 * @type String
 */ 
 ip;
/**
 * 玩家的平均网络延迟时间（ms）
 * @type Integer
 */ 
 avgPing;
/**
 * 玩家的平均网络丢包率（%）
 * @type Float
 */ 
 avgPacketLoss;
/**
 * 玩家设备的操作系统类型
 * @type String
 */ 
 os;
/**
 * 玩家客户端的识别码ID
 * @type String
 */ 
 clientId;
  
  
  



}
